#include <string.h>
#include <stdio.h>

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			return ((char *)&s[i]);
		i++;
	}
	if (c == '\0')
		return ((char *)&s[i]);
	return (0);
}

int	main(void)
{
	printf("%s\n", ft_strchr("salamalekoumsalam", 'y'));
	printf("%s\n", strchr("salamalekoumsalam", 'y'));
	return (0);
}
